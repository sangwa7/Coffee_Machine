import java.util.Scanner;

class Main {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        int a = scanner.nextInt();
        int b = scanner.nextInt();
        int c = scanner.nextInt();
        int d = scanner.nextInt();

        System.out.print(--a + " ");
        System.out.print(--b + " ");
        System.out.print(--c + " ");
        System.out.print(--d + " ");
    }
}